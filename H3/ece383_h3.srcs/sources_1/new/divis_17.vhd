entity divis_17 is
    Port ( i_input : in STD_LOGIC_VECTOR (7 downto 0);
           o_divisible : out STD_LOGIC);
end divis_17;

architecture divis_17_arch of divis_17 is

begin
    o_divisible <= '1' when i_input(7 downto 4) = i_input(3 downto 0) else '0';

end divis_17_arch;
